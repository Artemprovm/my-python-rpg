import os 
from PIL import Image  #  Всегда в самом верху
# Настройка пути: находим папку, в которой лежит этот файл кода
BASE_DIR = os.path.dirname(os.path.abspath(__file__))

money = 100  # Создаем переменную для хранения золота
hp = 100 #Создаем переменную для здоровья
inventar = ["Зелье"]  # Создаем список предметов игрока, сразу даем "Зелье"
info = {  # Подробная база данных товаров с их ценой и уроном
    "Меч": {"Урон": 25, "Цена": 50, "группа": "Ближний бой", "картинка": "mech.png"},
    "Щит": {"Урон": 10, "Цена": 25, "картинка": "shit.png"},
    "Зелье": {"Урон": 0, "Цена": 20, "способность": "Исцеление", "Лечение": 20, "картинка": "zelie.png"},
    "Лук": {"Урон": 20, "Цена": 40, "группа": "Дальнобой", "картинка": "luk.png"},
    "Нож": {"Урон": 10, "Цена": 20, "группа": "Ближний бой", "картинка": "nozh.png"}
}
while True:  # Запускаем бесконечный цикл, чтобы игра не закрывалась сама
    # Спрашиваем у пользователя, что он хочет сделать, и сохраняем ответ в vidgor
    vidgor = input("Выбор:(1 - Торг, 2 - информация, 3 - Выход, 4 - инвентярь)")

    if vidgor == "1":  # Если игрок выбрал ТОРГОВЛЮ
        print("\nДоступные товары:", list(info.keys())) # Показываем ключи из словаря info
        torg = input("Что хочешь купить? (Нажми Enter, чтобы выйти в меню): ")

        # Проверка 1: Если игрок ничего не ввел
        if torg == "":
            continue  # Возвращаемся в начало меню (не закрываем игру)

        # Проверка 2: Есть ли такой товар в нашей базе данных info?
        if torg in info:
            price = info[torg]["Цена"]  # Берем цену конкретного товара из словаря
            
            # Проверка 3: Хватает ли у игрока денег на этот товар?
            if money >= price:
                money -= price          # Списываем деньги (money = money - price)
                inventar.append(torg)   # Добавляем название товара в список инвентаря
                print(f"Успешно! Вы купили {torg}. Списано {price} золотых.")
                try:
                    # Склеиваем путь к папке игры и имя файла картинки
                    polniy_put = os.path.join(BASE_DIR, info[torg]["картинка"]) 
                    
                    img = Image.open(polniy_put) # Открываем по ПОЛНОМУ пути
                    img.show()
                except Exception as e:
                    # Если не сработает, Python напишет точную причину ошибки
                    print(f"[Система]: Ошибка! Не удалось найти {info[torg]['картинка']}")
                    print(f"Я искал тут: {polniy_put}")
            else:
                # Если денег меньше, чем цена
                print(f"Недостаточно золота! Нужно {price}, а у вас всего {money}.")
        
        else:
            # Если ввели название, которого нет в словаре info
            print(f"Товара '{torg}' нет в ассортименте магазина.")
    elif vidgor =="2":
        vopros = input("О чем хочешь узнать? ")
        if vopros in info:
            # Выводим красиво, а не просто весь словарь
            item = info[vopros]
            print(f"--- {vopros} ---")
            for k, v in item.items():
                if k != "картинка": # Картинку текстом не выводим
                    print(f"{k}: {v}")
        else:
            print("Информации об этом предмете нет.")
    elif vidgor == "3":  # Если выбрали "Выход"
        print("Пока путник") # Прощаемся
        break  # Останавливаем цикл while, программа завершается

    elif vidgor == "4":  # Если выбрали "Инвентарь"
        print(inventar, "Деньги:", money) # Выводим список предметов и текущее число в money

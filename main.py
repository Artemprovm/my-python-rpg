import os # Импортируем модуль для работы с путями файлов
import shop # Подключаем твой файл shop.py (торговец)
import hunt # Подключаем твой файл hunt.py (охота)
import casino # Подключаем казино

# Находим путь к папке, где лежит этот файл, чтобы открывать картинки
BASE_DIR = os.path.dirname(os.path.abspath(__file__))

# Создаем основные переменные игрока
damage = 0 # Начальный урон (сила атаки)
money = 100 # Стартовое золото
hp = 100 # Здоровье (максимум 100)
inventar = ["Зелье"] # Список вещей, по умолчанию даем одно зелье

# Общая база данных всех предметов в игре
info = {
    "Меч": {"Урон": 25, "Цена": 50, "группа": "Ближний бой", "картинка": "mech.png"},
    "Зелье": {"Урон": 0, "Цена": 20, "способность": "Исцеление", "Лечение": 20, "картинка": "zelie.png"},
    "Лук": {"Урон": 20, "Цена": 40, "группа": "Дальнобой", "картинка": "luk.png"},
    "Нож": {"Урон": 10, "Цена": 20, "группа": "Ближний бой", "картинка": "nozh.png"}
}

while True: # Запускаем главный цикл игры
    # Показываем статус игрока перед каждым выбором
    print(f"\n--- СТАТУС: HP: {hp} | Урон: {damage} | Золото: {money} ---")
    vidgor = input("Выбор: 1-Торг, 2-Инфо, 3-Выход, 4-Инвентарь, 5-Охота, Казино-6: ")

    if vidgor == "1": # Если выбрали ТОРГОВЛЮ
        # Вызываем функцию из файла shop.py и обновляем деньги и урон
        money, damage = shop.start_trade(money, damage, inventar, info, BASE_DIR)

    elif vidgor == "2": # Если выбрали ИНФОРМАЦИЮ (то, что ты спрашивал)
        vopros = input("О чем хочешь узнать? (Название предмета): ") # Просим ввести название
        if vopros in info: # Проверяем, есть ли такой предмет в словаре info
            print(f"--- Свойства {vopros} ---") # Заголовок
            for k, v in info[vopros].items(): # Перебираем все свойства (ключ и значение)
                if k != "картинка": # Картинку текстом не выводим
                    print(f"{k}: {v}") # Печатаем свойство, например "Урон: 25"
        else: # Если предмета нет в базе
            print("Информации об этом предмете нет.")

    elif vidgor == "3": # Если выбрали ВЫХОД
        print("До скорой встречи, герой!") # Прощальное сообщение
        break # Останавливаем цикл и закрываем программу

    elif vidgor == "4": # Если выбрали инвентарь
        print(f"В сумке: {inventar} | HP: {hp}") # Показываем вещи и здоровье
        # Спрашиваем игрока, приводим ввод к маленьким буквам для удобства
        otvet = input("Что использовать? (Название, 'да' для зелья или 'нет' для выхода): ").lower()

        # 1. Проверка на ОТМЕНУ
        if otvet == "нет" or otvet == "no" or otvet == "":
            print("Вы закрыли сумку.")
            continue # Возврат в главное меню

        # 2. Проверка на СОГЛАСИЕ (быстрое использование зелья)
        if otvet == "да" or otvet == "yes":
            use_item = "Зелье" # Сами подставляем название
        else:
            use_item = otvet.capitalize() # Иначе делаем первую букву большой (меч -> Меч)

        # 3. ПРОВЕРКА НАЛИЧИЯ И ИСПОЛЬЗОВАНИЕ
        if use_item in inventar: # Если предмет есть в списке
            # Проверяем, есть ли у него свойство "Лечение" в словаре info
            if "Лечение" in info.get(use_item, {}): 
                hp = min(100, hp + info[use_item]["Лечение"]) # Лечимся, но не больше 100
                inventar.remove(use_item) # Удаляем потраченную вещь
                print(f"Вы использовали {use_item}. Теперь ваше здоровье: {hp}")
            else:
                # Если предмет есть, но он не лечит (например, Меч)
                print(f"{use_item} нельзя 'использовать', он дает пассивный урон.")
        else:
            # Если предмета вообще нет в сумке
            print(f"У вас нет предмета '{use_item}'!")

    elif vidgor == "5": # Если выбрали ОХОТУ
        # Вызываем функцию из файла hunt.py и обновляем HP и деньги
        hp, money = hunt.start_hunt(hp, damage, money)
        if hp <= 0: # Проверка: если после боя HP упало до 0
            print("К сожалению, вы погибли в бою... ИГРА ОКОНЧЕНА.")
            break # Завершаем игру

    elif vidgor == "6": # Если выбрали Казино
     # Вызываем функцию и сохраняем результат в переменную money
     money = casino.start_gamble(money)

